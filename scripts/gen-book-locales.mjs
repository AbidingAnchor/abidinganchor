const keys = Object.keys(en)
for (const k of ['es', 'pt', 'fr', 'de']) {
  const m = { en, es, pt, fr, de }[k]
  for (const key of keys) {
    if (m[key] === undefined) console.error('missing', k, key)
  }
}

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const localesDir = path.join(__dirname, '../src/locales')
const byLang = { en, es, pt, fr, de }

for (const [code, books] of Object.entries(byLang)) {
  const fp = path.join(localesDir, `${code}.json`)
  const data = JSON.parse(fs.readFileSync(fp, 'utf8'))
  data.bible = data.bible || {}
  data.bible.books = books
  fs.writeFileSync(fp, `${JSON.stringify(data, null, 2)}\n`)
}
console.log('Merged bible.books into', Object.keys(byLang).join(', '))
