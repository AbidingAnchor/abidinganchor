function deepMerge(a, b) {
  const out = { ...a }
  for (const k of Object.keys(b)) {
    const bv = b[k]
    const av = out[k]
    if (
      bv &&
      typeof bv === 'object' &&
      !Array.isArray(bv) &&
      av &&
      typeof av === 'object' &&
      !Array.isArray(av)
    ) {
      out[k] = deepMerge(av, bv)
    } else {
      out[k] = bv
    }
  }
  return out
}

